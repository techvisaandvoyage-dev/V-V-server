const normalize = (value) =>
  String(value || "")
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, " ")
    .trim();

const codeToFlagEmoji = (code) =>
  String(code || "")
    .toUpperCase()
    .replace(/./g, (char) => String.fromCodePoint(127397 + char.charCodeAt(0)));

const isFlagEmoji = (value) => /^\p{Regional_Indicator}{2}$/u.test(String(value || ""));

export const getCountryFlagEmoji = (countryName, fallback) => {
  const code = COUNTRY_CODES[countryName];
  if (code) return codeToFlagEmoji(code);
  return isFlagEmoji(fallback) ? fallback : "🌍";
};

/** ISO 3166-1 alpha-2 when the canonical English name matches our map */
export const getIsoAlpha2FromCountryName = (countryName) =>
  COUNTRY_CODES[countryName] || null;

/** Top-left code chip on cards: real ISO when known (avoids slug quirks like "ca" from "canada"). */
export const getCountryCardCodeBadge = (country) => {
  const iso = country?.name ? getIsoAlpha2FromCountryName(country.name) : null;
  if (iso) return iso;
  const id = String(country?.id || "").replace(/[^a-z0-9]/gi, "");
  if (id.length >= 2) return id.slice(0, 2).toUpperCase();
  return "--";
};

export const matchesCountrySearch = (country, rawTerm) => {
  const term = normalize(rawTerm);
  if (!term) return true;

  const values = [
    country.name,
    country.id,
    country.slug,
    country.continent,
    country.visaType,
  ].map(normalize);

  if (term.length >= 2) {
    values.push(...(COUNTRY_ALIASES[country.name] || []).map(normalize));
  }

  return values.some((value) => value.includes(term));
};

export const getCountrySearchHint = (country, rawTerm) => {
  const term = normalize(rawTerm);
  if (!term) return "";

  const alias = (COUNTRY_ALIASES[country.name] || []).find((value) =>
    normalize(value).includes(term)
  );

  return alias ? `Matches ${alias}` : "";
};
